function operations(x, y, operator) {
    let result;
    switch (operator) {
        case '+':
            result = x + y;
            break;
        case '-':
            result = x - y;
            break;
        case '/':
            result = x / y;
            break;
        case '*':
            result = x * y;
            break;
        case '%':
            result = x % y;
            break;
        case '**':
            result = x ** y;
            break;
    }
    console.log(result);
}

operations(4, 3, '+');
operations(6, 5.5, '*');
